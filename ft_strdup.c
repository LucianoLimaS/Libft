#include "libft.h"

char	*ft_strdup(const char *s)
{
	void	*ptr;

	ptr = malloc(sizeof(s));
	if (!ptr)
		return (NULL);
	ft_strlcpy(ptr, s, sizeof(s) * ft_strlen(s));
	return (ptr);
}
